package math

import (
	"jvmgo/instruction"
	"jvmgo/rtda"
)

// Multiply double
type DMUL struct {
	instruction.NoOperandsInstruction
}

func (self *DMUL) Execute(frame *rtda.Frame) {
	stack := frame.OperandStack()
	v2 := stack.PopDouble()
	v1 := stack.PopDouble()
	result := v1 * v2
	stack.PushDouble(result)
}

